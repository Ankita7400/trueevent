<script>
  document.addEventListener('DOMContentLoaded', function () {
    const modal1 = new bootstrap.Modal(document.getElementById('exampleModalToggle'));
    const modal2 = new bootstrap.Modal(document.getElementById('exampleModalToggle2'));

    modal1.show();

    const form = document.getElementById('userForm');

    form.addEventListener('submit', async function (e) {
      e.preventDefault(); // Prevent default form submission

      const formData = new FormData(form);

      try {
        const response = await fetch(form.action, {
          method: 'POST',
          headers: {
            'X-CSRF-TOKEN': document.querySelector('input[name="_token"]').value
          },
          body: formData
        });

        if (response.ok) {
          modal1.hide();
          setTimeout(() => modal2.show(), 500);
          form.reset(); // Optional: reset form after successful submission
        } else {
          const data = await response.json();
          alert("Submission failed. Check input or try again.");
          console.log(data);
        }

      } catch (error) {
        alert("An error occurred. Try again.");
        console.error(error);
      }
    });
  });
</script>
